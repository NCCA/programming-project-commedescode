#ifndef WINDOWPARAMS_H_
#define WINDOWPARAMS_H_

//----------------------------------------------------------------------------------------------------------------------
/// @file WindowParams.h
/// @brief Structure to hold window parameters for mouse and window control
/// @author [Author Name]
/// @version 1.0
/// @date [Date]
//----------------------------------------------------------------------------------------------------------------------

struct WindowParams
{
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief Used to store the X rotation mouse value
  //----------------------------------------------------------------------------------------------------------------------
  int spinXFace = 0;

  //----------------------------------------------------------------------------------------------------------------------
  /// @brief Used to store the Y rotation mouse value
  //----------------------------------------------------------------------------------------------------------------------
  int spinYFace = 0;

  //----------------------------------------------------------------------------------------------------------------------
  /// @brief Flag to indicate if the mouse button is pressed when dragging
  //----------------------------------------------------------------------------------------------------------------------
  bool rotate = false;

  //----------------------------------------------------------------------------------------------------------------------
  /// @brief Flag to indicate if the right mouse button is pressed when dragging
  //----------------------------------------------------------------------------------------------------------------------
  bool translate = false;

  //----------------------------------------------------------------------------------------------------------------------
  /// @brief The previous X mouse value
  //----------------------------------------------------------------------------------------------------------------------
  int originalX = 0;

  //----------------------------------------------------------------------------------------------------------------------
  /// @brief The previous Y mouse value
  //----------------------------------------------------------------------------------------------------------------------
  int originalY = 0;

  //----------------------------------------------------------------------------------------------------------------------
  /// @brief The previous X mouse value for position changes
  //----------------------------------------------------------------------------------------------------------------------
  int originalXPosition = 0;

  //----------------------------------------------------------------------------------------------------------------------
  /// @brief The previous Y mouse value for position changes
  //----------------------------------------------------------------------------------------------------------------------
  int originalYPosition = 0;

  //----------------------------------------------------------------------------------------------------------------------
  /// @brief Window width
  //----------------------------------------------------------------------------------------------------------------------
  int width = 1024;

  //----------------------------------------------------------------------------------------------------------------------
  /// @brief Window height
  //----------------------------------------------------------------------------------------------------------------------
  int height = 720;
};

//----------------------------------------------------------------------------------------------------------------------
/// @brief The increment for X/Y translation with mouse movement
//----------------------------------------------------------------------------------------------------------------------
constexpr float INCREMENT = 0.01f;

//----------------------------------------------------------------------------------------------------------------------
/// @brief The increment for the wheel zoom
//----------------------------------------------------------------------------------------------------------------------
constexpr float ZOOM = 0.1f;

#endif // WINDOWPARAMS_H_